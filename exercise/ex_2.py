# prostokat
a = 10
b = 5

obwod_p = 2 * a + 2 * b

pole_p = a * b

print("Obwod prostokata wynosi " + str(obwod_p) + ", a pole " + str(pole_p) + ".")

# trapez
h = 4
c = 5
d = 6

obwod_t = a + b + c + d
pole_t = ((a + b) * h) / 2
print("Obwod trapezu wynosi " + str(obwod_t) + ", a pole " + str(pole_t) + ".")

# romb

obwod_r = 4 * a
pole_r = (a * h)
print("Obwod rombu wynosi " + str(obwod_r) + ", a pole " + str(pole_r) + ".")

# koło

r = 4
obwod_k = 2 * 3.14 * r
pole_k = r * r * 3.14
print("Obwod koła wynosi " + str(obwod_k) + ", a pole " + str(pole_k) + ".")

